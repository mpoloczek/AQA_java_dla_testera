public class Zmienne {
    public static void main(String[] args) {

        //Zmienna dla liczby całkowitej
        int number10; //deklaracja zmiennej
        number10 = 10; // inicjalizacja zmiennej
        System.out.println(number10);

        int number11 = 11; //deklaracja + inicjalizacja zmiennej
        System.out.println(number11);

        //Zmienna dla liczby zmiennoprzecinkowej
        double doubleNumber = 10.5;
        System.out.println(doubleNumber);

        //Zmienna dla tekst
        String someText = "to jest jakiś tekst";
        System.out.println(someText);

        //Zmienna dla pojedyńczy znak
        char someChar = 's';
        System.out.println(someChar);

        //Zmienna dla prawda lub fałsz
        boolean trueValue = true;
        System.out.println(trueValue);
        boolean falseValue = false;
        System.out.println(falseValue);

    }
}
